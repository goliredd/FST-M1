import pytest
def test_sumofnum():
    num1 = 10
    num2 = 5
    sum = num1 + num2
    assert sum == 15

def test_diffofnum():
    num1 = 10
    num2 = 5
    diff = num1 - num2
    assert diff == 5

def test_productofnum():
    num1 = 10
    num2 = 5
    product = num1 * num2
    assert product == 50

def test_quotientofnum():
    num1 = 10
    num2 = 5
    quotient = num1 / num2
    assert quotient == 2

